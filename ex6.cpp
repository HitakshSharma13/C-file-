// C++ program  to calculate multiplication of two numbers

#include<iostream>
#include<cmath>
using namespace std;

int main()
{
    double a, b, product;

    cout<<"Enter the value of a is: ";
    cin>>a;

    cout<<"Enter the value of b is: ";
    cin>>b;

    product = (a*b);

    cout<<"The product of two numbers a "<<a<<" and b "<<b<<" is: "<<product<<endl;
    return 0;
}