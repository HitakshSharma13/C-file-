// C++ program to add two numbers

#include<iostream>
#include<cmath>
using namespace std;

int main()
{
    int a, b, sum;
    cout<<"Enter the value of a is: ";
    cin>>a;
    cout<<"Enter the value of b is: ";
    cin>>b;
    sum = a+b;

    cout<<"The sum of the two number a is"<<a<<" and b is "<<b<<" and sum is"<<sum<<endl;
    return 0;
}