// C++ program to find Square root of a number.

#include<iostream>
#include<cmath>
using namespace std;

int main()
{
    int a; 
    float result;
    cout<<"Enter the number a is: ";
    cin>>a;
    result = sqrt(a);
    cout<<"The square root of the number "<<a<<" is: "<<result<<endl;
    return 0;
}